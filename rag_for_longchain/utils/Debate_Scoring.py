# -*- coding: utf-8 -*-

import os
import yaml
import re
import requests

class DebateScoring:
    CONFIG_PATH = "D:/converstional_rag/rag_for_longchain/config/config.yaml"
    SCORING_CRITERIA_PATH = "D:/converstional_rag/rag_for_longchain/utils/mark_debate_skill.txt"

    def __init__(self):
        """
        初始化评分类，通过加载外部文件中的评分细则和 API 配置。
        """
        self.scoring_criteria = self._load_scoring_criteria(self.SCORING_CRITERIA_PATH)

        self._load_config()

    def _load_config(self):
        """
        从 YAML 配置文件中加载 API 配置。
        """
        if not os.path.exists(self.CONFIG_PATH):
            raise FileNotFoundError(f"配置文件 '{self.CONFIG_PATH}' 不存在。")

        with open(self.CONFIG_PATH, 'r', encoding='utf-8') as f:
            config = yaml.safe_load(f)
            openai_config = config.get("openai", {})
            self.api_key = openai_config.get("api_key")
            self.api_base = openai_config.get("api_base", "https://api.openai.com/v1")
            self.model = openai_config.get("llm_model", "gpt-4")

    def _load_scoring_criteria(self, path):
        """
        从指定文件加载评分细则。

        参数:
        path: str
            包含评分细则的文件路径。

        返回:
        dict
            一个字典，其中键是评分标准的名称，值是评分标准的描述。
        """
        if not os.path.exists(path):
            raise FileNotFoundError(f"评分细则文件 '{path}' 不存在。")

        scoring_criteria = {}
        with open(path, 'r', encoding='utf-8') as file:
            for line in file:
                if ":" in line:
                    key, value = line.strip().split(":", 1)
                    scoring_criteria[key.strip()] = value.strip()

        return scoring_criteria

    def score_labels_for_dataset(self, input_dataset):
        """
        使用 GPT 为输入数据集中的每条数据评分，并附加到原始数据中。

        参数:
        input_dataset: list
            包含多条数据的列表，每条数据是一个字典，包含 "stance", "debater", "utterance" 和 "labels"。

        返回:
        list
            一个包含标签及其分数的字典列表。
        """
        scored_dataset = []

        for data in input_dataset:
            labels_dict = data.get("labels", {})  # 嵌套字典格式
            if not isinstance(labels_dict, dict):
                raise TypeError(f"Labels 应为字典格式，但收到的是 {type(labels_dict)} 类型：{labels_dict}")

            scored_labels = self._process_labels_dict(labels_dict)
            scored_dataset.append({"labels": scored_labels})

        return scored_dataset

    def _process_labels_dict(self, labels_dict):
        """
        处理 Labels 字典，提取每个辩论技巧并生成评分。

        参数:
        labels_dict: dict
            包含对辩论技巧详细解释的字典。

        返回:
        list
            每个辩论技巧及其评分的列表。
        """
        scored_techniques = []

        for technique_name, technique_reason in labels_dict.items():
            prompt = self._generate_prompt_for_technique(technique_name)
            score = self._get_gpt_score(prompt)
            scored_techniques.append({"technique": technique_name.strip(), "score": round(score, 1)})

        return scored_techniques

    def _generate_prompt_for_technique(self, technique_name):
        """
        为 GPT 生成评分提示，仅针对辩论技巧。

        参数:
        technique_name: str
            技巧名称。

        返回:
        str
            格式化的提示字符串。
        """
        prompt = f"""
        你是一位从业20年的辩论专家，请根据以下评分标准为辩论技巧打分,每项打分（1-10）,其中总体分数为其余四项分数的平均数:

        技巧名称: {technique_name}
        输出要求：
        格式严格为：
        - 相关性（Relevance）: X.X
        - 综合性（Integration）: X.X
        - 创新性（Creativity）: X.X
        - 反驳有效性（Rebuttal Effectiveness）: X.X
        - 总体分数: X.X


        请不要添加任何多余内容
        评分标准:
        """
        for criterion, description in self.scoring_criteria.items():
            prompt += f"- {criterion}: {description}\n"


        return prompt

    def _get_gpt_score(self, prompt):
        """
        使用 GPT 根据生成的提示对标签评分。

        参数:
        prompt: str
            要发送给 GPT 的提示。

        返回:
        float
            GPT 提供的平均分数。
        """
        headers = {
            "Authorization": f"Bearer {self.api_key}",
            "Content-Type": "application/json"
        }
        data = {
            "model": self.model,
            "messages": [
                {"role": "system", "content": "You are a debate expert with 20 years of experience."},
                {"role": "user", "content": prompt}
            ],
            "max_tokens": 50,  # 确保响应长度
            "temperature": 0.5  # 降低创造性，保证一致性
        }

        try:
            response = requests.post(f"{self.api_base}/chat/completions", headers=headers, json=data)
            response.raise_for_status()
            response_data = response.json()
            response_text = response_data["choices"][0]["message"]["content"].strip()
            # print(f"Prompt sent to GPT: {prompt}")
            # print(f"Response from GPT: {response_data}")

            # 解析分数
            match = re.search(r'分数[:：]?\s?(\d+(\.\d+)?)', response_text)
            if match:
                return float(match.group(1))

            # 如果没有提取到分数
            print(f"未能从响应中提取分数: {response_text}")
            return 0.0

        except Exception as e:
            print(f"GPT 评分时发生错误: {e}")
            return 0.0


